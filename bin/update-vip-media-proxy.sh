#!/usr/bin/env bash
# Generate DDEV nginx config to redirect missing uploads to VIP (like dev-env --media-redirect-domain).
set -euo pipefail

ROOT="$(cd "$(dirname "$0")/.." && pwd)"
CONFIG="${ROOT}/config/vip-sync.yaml"
NGINX_DIR="${ROOT}/.ddev/nginx"
NGINX_CONF="${NGINX_DIR}/vip-media-proxy.conf"

QUIET=false

usage() {
	cat <<EOF
Usage: $(basename "$0") [options]

Write .ddev/nginx/vip-media-proxy.conf for missing /wp-content/uploads/ files.
Equivalent to VIP dev-env --media-redirect-domain.

Options:
  -q, --quiet    Suppress non-error output
  -h, --help     Show this help

Domain resolution (first match):
  1. media_redirect_domain in config/vip-sync.yaml
  2. VIP-CLI siteurl on @app.production (media usually lives on production)
  3. Primary domain from config/.vip.<app>.<env>.yml domain_map keys
  4. VIP-CLI siteurl on @app.<env>

Run ddev restart after updating nginx config.
EOF
}

yaml_value() {
	local key="$1" file="$2"
	local line
	line="$(grep -E "^${key}:" "${file}" | head -1 || true)"
	if [[ -z "${line}" ]]; then
		return 1
	fi
	echo "${line#*:}" | sed -E 's/^[[:space:]]+//; s/[[:space:]]+$//; s/^["'\''](.*)["'\'']$/\1/'
}

log() {
	if [[ "${QUIET}" != true ]]; then
		echo "$@"
	fi
}

find_vip_data_sync_config() {
	local app="$1" env="$2"
	local exact="${ROOT}/config/.vip.${app}.${env}.yml"
	local match

	if [[ -f "${exact}" ]]; then
		echo "${exact}"
		return 0
	fi

	match="$(find "${ROOT}/config" -maxdepth 1 -name ".vip.${app}.${env}.*.yml" 2>/dev/null | head -1 || true)"
	if [[ -n "${match}" ]]; then
		echo "${match}"
		return 0
	fi

	return 1
}

# Hostname only: www.example.com
normalize_media_host() {
	local input="$1"
	local host

	input="${input%/}"
	if [[ "${input}" == http*://* ]]; then
		host="$(ruby -ruri -e 'puts URI(ARGV[0]).host' "${input}" 2>/dev/null || true)"
	else
		host="${input%%/*}"
	fi

	host="${host#http://}"
	host="${host#https://}"
	echo "${host}"
}

vip_wp_option_siteurl() {
	local app="$1" env="$2"
	vip "@${app}.${env}" --yes -- wp option get siteurl 2>/dev/null | tr -d '\r' || true
}

primary_domain_from_data_sync() {
	local file="$1"
	ruby -ryaml -rpathname -e '
		data = YAML.load_file(Pathname.new(ARGV[0]))
		map = data&.dig("data_sync", "domain_map")
		exit 1 unless map.is_a?(Hash) && !map.empty?
		# Prefer the last map key (often the root production domain; specific entries come first).
		puts map.keys.last
	' "${file}" 2>/dev/null || true
}

resolve_media_host() {
	local app="$1" env="$2"
	local configured sync_config siteurl host

	if [[ -f "${CONFIG}" ]]; then
		configured="$(yaml_value media_redirect_domain "${CONFIG}" 2>/dev/null || true)"
		if [[ -n "${configured}" && "${configured}" != "false" ]]; then
			normalize_media_host "${configured}"
			return 0
		fi
	fi

	siteurl="$(vip_wp_option_siteurl "${app}" "production")"
	host="$(normalize_media_host "${siteurl}")"
	if [[ -n "${host}" ]]; then
		echo "${host}"
		return 0
	fi

	if sync_config="$(find_vip_data_sync_config "${app}" "${env}")"; then
		host="$(normalize_media_host "$(primary_domain_from_data_sync "${sync_config}")")"
		if [[ -n "${host}" ]]; then
			echo "${host}"
			return 0
		fi
	fi

	siteurl="$(vip_wp_option_siteurl "${app}" "${env}")"
	host="$(normalize_media_host "${siteurl}")"
	if [[ -n "${host}" ]]; then
		echo "${host}"
		return 0
	fi

	return 1
}

disable_media_proxy() {
	if [[ -f "${NGINX_CONF}" ]]; then
		rm -f "${NGINX_CONF}"
		log "Removed ${NGINX_CONF#"${ROOT}/"} (media redirect disabled)."
	fi
}

write_media_proxy() {
	local host="$1"

	mkdir -p "${NGINX_DIR}"
	cat >"${NGINX_CONF}" <<EOF
# Generated by bin/update-vip-media-proxy.sh — do not edit.
# VIP dev-env equivalent of --media-redirect-domain=${host}
# https://docs.wpvip.com/vip-local-development-environment/add-media-content/

location ^~ /wp-content/uploads/ {
    try_files \$uri @vip_media_redirect;
}

location @vip_media_redirect {
    return 302 https://${host}\$request_uri;
}
EOF

	log "Wrote ${NGINX_CONF#"${ROOT}/"} (missing uploads -> https://${host})"
	log "Run: ddev restart"
}

while [[ $# -gt 0 ]]; do
	case "$1" in
		-q | --quiet)
			QUIET=true
			shift
			;;
		-h | --help)
			usage
			exit 0
			;;
		*)
			echo "ERROR: Unknown option: $1"
			usage
			exit 1
			;;
	esac
done

if [[ ! -f "${CONFIG}" ]]; then
	disable_media_proxy
	exit 0
fi

APP="$(yaml_value app "${CONFIG}" 2>/dev/null || true)"
ENV="$(yaml_value env "${CONFIG}" 2>/dev/null || true)"

if [[ -z "${APP}" || -z "${ENV}" ]]; then
	disable_media_proxy
	exit 0
fi

if ! command -v vip >/dev/null 2>&1; then
	echo "WARN: vip not in PATH; skipping media proxy update." >&2
	exit 0
fi

if MEDIA_HOST="$(resolve_media_host "${APP}" "${ENV}")"; then
	write_media_proxy "${MEDIA_HOST}"
else
	echo "WARN: Could not resolve media redirect domain; media proxy disabled." >&2
	echo "      Set media_redirect_domain in config/vip-sync.yaml" >&2
	disable_media_proxy
	exit 0
fi
